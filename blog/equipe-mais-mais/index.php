        <p> Nós tivemos pessoas fantásticas se juntando a nós nos últimos meses (e nós ainda estamos <a href="https://jobs.lever.co/openai">contratando</a>). Bem vindos! </p>

<hr>

<h2> Tempo Integral </h2>

<ul class='HiresList'>
  <li><strong>Yura Burda.</strong> <a href="https://sites.google.com/site/yburda/">Yura</a> Yura terminou um doutorado de matemática aos 24 anos de idade, e mudou para a área de machine learning há um ano e meio. Ele está se concentrando em <a href="https://en.wikipedia.org/wiki/Generative_model">modelos generativos</a>. Ele <a href="http://arxiv.org/abs/1509.00519">descobriu</a> uma melhoria simples mas fundamental para o limite inferior variacional, que não se tinha notícias desde de sua descoberta original, décadas atrás.</li>

  <li> <strong>Ian Goodfellow.</strong> <a href="https://www.linkedin.com/in/ian-goodfellow-b7187213">Ian</a> é bem conhecido por suas muitas contribuições ao campo de machine learning, incluindo a <a href="http://arxiv.org/abs/1302.4389">Rede MaxOut</a> e a <a href="http://arxiv.org/abs/1406.2661">Rede Adversária Generativa</a>, a última no qual é um motor de empolgação na pesquisa de modelagem generativa. Além disso, ele é o principal autor do <a href="http://www.deeplearningbook.org/">livro</a> sobre deep learning. </li>

  <li> <strong>Alec Radford.</strong> <a href="https://twitter.com/alecrad">Alec</a> criou o <a href="http://arxiv.org/abs/1511.06434">DCGAN</a>, um modelo neural que pode gerar imagens grandes e coerentes, contendo um nível sem precedentes de coerência global e detalhe. Além disso, seu modelo aprendeu a fazer <a href="https://github.com/Newmu/dcgan_code#arithmetic-on-faces">analogias de imagem</a> de uma maneira totalmente sem supervisão. </li>

  <li> <strong>Tim Salimans.</strong> <a href="http://timsalimans.com/">Tim</a> é um especialista em métodos variacionais. O autor do primeiro <a href="http://projecteuclid.org/euclid.ba/1386166315">artigo</a> sobre a inferência variacional do gradiente estocástico (para o qual ele ganhou o <a href="https://bayesian.org/awards/LindleyPrize.html">prêmio Lindley</a>), ele foi em um momento <a href="https://www.kaggle.com/timsalimans">número 2 global</a> no rank do Kaggle.
</li>
</ul>


<hr>

<h2> Internos </h2>

<p>Também se juntam a nós para o verão (e, em alguns casos, para continuar a colaboração, uma vez que retornam à sua instituição de origem):</p>

<ul class='HiresList'>
  <li> <strong>Peter Chen.</strong> <a href="https://www.linkedin.com/in/peter-xi-chen-25178356">Peter</a> anteriormente co-fundou uma <a href="https://angel.co/sellegit-1">startup</a> e fez pesquisas sobre computação paralela e ciência cognitiva. Recentemente, trabalhou em reinforcement learning, da teoria às aplicações com deep neural nets. </li>

  <li> <strong>Rocky Duan.</strong> <a href="https://www.linkedin.com/in/rocky-duan-1a82662a">Rocky</a> é um estudante de doutorado que anteriormente co-fundou uma  <a href="https://angel.co/sellegit-1">startup</a> (com Peter) e trabalhou em uma série de projetos em robótica. Ele agora mudou para o campo de deep reinforcement learning, e recentemente fez o curso de <a href="http://rll.berkeley.edu/deeprlcourse/">Deep Reinforcement Learning</a> em Berkeley. </li>

  <li> <strong>Linxi Fan.</strong> Embora <a href="http://eona.co/">Linxi</a> estar apenas terminando sua graduação, ele já trabalhou em uma <a href="http://arxiv.org/abs/1511.06430">variedade</a> de projetos em deep learning, incluindo o <a href="http://arxiv.org/abs/1512.02595">Deep Speech 2</a>. </li>

    <li> <strong>Jon Gauthier.</strong> <a href="https://twitter.com/j_gauthier">Jon</a> é um aluno de graduação cujas conquitas incluem, entre outros, um <a href="http://arxiv.org/pdf/1603.06021v1.pdf">método</a> que torna possível treinar eficientemente grandes recursive neural networks em grandes conjuntos de dados. </li>

  <li> <strong>Jonathan Ho.</strong> <a href="https://www.linkedin.com/in/jonathan-ho-00243623">Jonathan</a> é um estudante de PhD em Stanford, onde ele está desenvolvendo uma nova abordagem para imitation learning. Anteriorment em sua carreira de pesquisa, ele teve sucesso <a href="http://rll.berkeley.edu/isrr2013lfd/">ensinando robôs como amarrar nós</a>. </li>

  <li> <strong>Rein Houthooft.</strong> <a href="http://reinhouthooft.github.io/">Rein</a> é oriundo da Bélgica, iniciou sua carreira de pesquisador no campo de redes de computadores, e agora está desenvolvendo novas formas de incorporar a incerteza em algoritmos de deep reinforcement learning. </li>
    
  <li> <strong>Eric Price.</strong> <a href="http://www.cs.utexas.edu/~ecprice/">Eric</a> é professor de ciência da computação teórica na UT Austin. Suas conquistas incluem o desenvolvimento e a análise de uma <a href="http://dspace.mit.edu/openaccess-disseminate/1721.1/73474">transformação de Fourier mais-rápida-que-<em>n log n</em></a>. No passado ele conseguiu uma <a href="http://www.ioinformatics.org/locations/ioi05/contest/results.pdf">pontuação perfeita</a> no IOI. </li> 
</ul>


