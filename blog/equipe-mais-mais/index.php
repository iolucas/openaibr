<p>Atualmente estamos focados em unsupervised learning e reinforcement learning. Devemos ter resultados interessantes para compartilhar ao longo do próximo mês ou dois. Um grupo de nós estará em torno do <a href="http://www.iclr.cc/">ICLR</a>, onde provavelmente iremos realizar um evento de alguma forma. Também serei anfitrião de uma <a href="https://productupdates.quora.com/Introducing-Sessions">Sessão no Quora</a> em maio ou junho para responder a quaisquer perguntas para pessoas que não conhecemos em Porto Rico.</p>
  

        
