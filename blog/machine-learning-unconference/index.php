</p>
<hr />
      
<p> Machine learning está se movendo incrivelmente rápido. Para acompanhar, muitos praticantes passam várias semanas por ano em conferências. Contudo, as apresentações nas conferências são todas sobre trabalhos apresentados meses antes, o que significa que as pessoas já estão intimamente familiarizadas com o conteúdo (e muitas vezes já foi ultrapassado). </p>
      
      
      
<p>  Gostaríamos de tentar em vez disso, hospedar um evento focado na parte mais valiosa de qualquer conferência: as pessoas. Por favor, junte-se a nós para o nosso primeiro Machine Learning Unconference, um encontro experimental conduzido por seus participantes, em vez de um comitê organizador. </p>      
      
      
<p>
A <a href="https://en.wikipedia.org/wiki/Unconference">unconference</a>
será um evento gratuito no escritório da OpenAI em San Francisco
na <strong>sexta-feira e sábado, 7-8 de outubro de 2016</strong>. Serão bem vindos participantes de todo o mundo.
A partir de 22 de agosto, nós terminamos de aceitar aplicações para a unconference.


<h2> Por que ter uma unconference em machine learning? </h2>
      
<p> Conferências desempenham dois papéis principais: reunião social e publicação. Nós sentimos que estes dois papéis são um tanto ortogonais e podem ser melhor servidos separados. </p>
      
      
      
<p> Na maioria das conferências, o tempo social não estruturado, como as refeições, é o que as pessoas acham valioso. Queremos maximizar a quantidade de interatividade em uma reunião social de pesquisadores. Uma
unconference minimamente estruturada fornece uma oportunidade para um experimento, para ver que tipos de novos formatos os participantes irão desenvolver e descobrir quais são os mais eficazes. </p>
      
      
<p>  Também temos idéias sobre como melhorar o sistema de revisão e publicação de artigos, que estamos desenvolvendo separadamente. </p>

<h2> Agenda </h2>
      
<p>  Seria ir contra o espírito da unconference se fornecermos uma agenda específica, mas sugerimos que os participantes usem
o <a href="https://gitter.im/openai/mlu16">bate-papo Gitter</a> para planejar alguns tópicos de discussão com antecedencia. </p>

<p>
Todos os participantes também são incentivados a trazer um poster descrevendo um trabalho de machine
learning que seria interessante ou útil para outros participantes aprenderem. (Seguiremos com mais detalhes sobre o formato por e-mail.)
</p>

<h2> Quem deve vir? </h2>
      
<p> Qualquer pessoa que esteja envolvida em pesquisas ou aplicações de machine learning é bem-vinda. Esperamos especialmente ver:</p>
<ul>
  <li> Estudantes de doutorado, membros de corpo docente e cientistas da indústria.</li>

  <li> Engenheiros de software usando machine learning em aplicações ou construindo uma infra-estrutura de machine learning.</li>

</ul>

      
<p> Estamos particularmente animados em apoiar mulheres, minorias e membros de outros grupos sub-representados em machine learning. A OpenAI fornecerá um número limitado de subsídios de viagem e alojamento para os membros destes grupos.
</p>
      

<p>Este evento destina-se principalmente a pessoas com fluência técnica
em machine learning para ajudar uns aos outros avançarem o estado da arte. Se você estiver interessado em aprender sobre o básico do campo, há uma abundância de outros <a href="http://ai.withthebest.com/">grandes</a> <a href="https://www.re-work.co/events/deep-learning-summit-san-francisco-2017">eventos</a>
que sugerimos. </p>

<h2> Logística </h2>
      

<p> A OpenAI está patrocinando e hospedando o evento, mas o evento em si não tem organização oficial &mdash; são apenas pessoas que acham este convite interessante, reunindo-se para discutir machine learning. </p>
      
<p> A OpenAI fornecerá almoço e lanches. Os participantes são responsáveis por organizar sua própria viagem e hospedagem. </p>

      
      
<h2> Participantes </h2>

<p> Ao participar, você concorda em seguir nosso <a href="https://openai.com/blog/machine-learning-unconference/code-of-conduct.txt">código de conduta</a>. </p>

<p> Gostaríamos de dar as boas-vindas a todos, mas só temos capacidade para 150 pessoas. Ajustaremos a lista de aceitação para assegurar uma conferência interessante com uma diversidade de pessoas e perspectivas. </p>

      
