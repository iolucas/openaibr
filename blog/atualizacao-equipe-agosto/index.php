<h2> Estagiários &amp; Visitantes </h2>

<p> Também estamos estimulados a trabalhar com as seguintes pessoas por um período de tempo mais limitado: </p>

<ul class='HiresList'>
        
  <li> <strong>Catherine Olsson</strong>. <a href="https://www.linkedin.com/in/catherineolsson">Catherine</a> construiu a <a href="https://github.com/openai/gym-http-api">API REST</a> do OpenAI Gym, que já atraiu usuários de <a href="https://github.com/openai/gym-http-api/tree/master/binding-lua">Lua</a>, <a href="https://github.com/openai/gym-http-api/tree/master/binding-cpp">C++</a>, <a href="https://github.com/deeplearning4j/gym-java-client">Java</a>, e <a href="https://github.com/openai/gym-http-api/tree/master/binding-rust">Rust</a>. Ela se formou com um GPA perfeito em Ciencia da Computação e Ciência Cognitiva e Científica no MIT, e tem vasta experiência em pesquisa de <a href="http://f1000research.com/posters/1098032">neurociência computacional</a> e <a href="https://scholar.google.com/citations?view_op=view_citation&hl=en&user=TvdMDhwAAAAJ&citation_for_view=TvdMDhwAAAAJ:2osOgNQ5qMEC">psicologia</a>. Catherine ensinou <a href="http://www.cns.nyu.edu/~eero/math-tools14/">programação e matemática aplicada</a> por seis anos, alcançando mulheres e minorias subrepresentadas. </li>


  <li><strong>Harri Edwards</strong> <a href="https://github.com/harri-edwards">Harri</a> é um estudante de doutorado na University of Edinburgh, onde ele está pesquisando modelos que podem rapidamente se adaptar a novas situações <a href="https://arxiv.org/abs/1606.02185">aprendendo a representar conjuntos de dados</a>.</li>

    
  <li><strong>Igor Mordatch.</strong> <a href="https://people.eecs.berkeley.edu/~igor.mordatch/">Igor</a> está interessado em controle ótimo, machine learning, e suas aplicações para robótica, biomecânica e neurociência. Seu doutorado foi em descoberta automatizada e aprendizagem de <a href="https://www.youtube.com/watch?v=mhr_jtQrhVA">comportamentos</a> de movimento complexos.  Ele se juntará à faculdade em <a href="http://www.cmu.edu/">CMU</a> em setembro de 2017.</li>
    
    
    
  <li><strong>Taco Cohen</strong> <a href="http://ta.co.nl/">Taco</a> é um estudante de doutorado que trabalha em problemas aplicados e teóricos em representation learning. Recentemente ele inventou a group equivariant convolutional neural networks (<a href="http://jmlr.org/proceedings/papers/v48/cohenc16.pdf">G-CNNs</a>), uma generalização de CNNs que melhora a eficiência estatística destes modelos explorando simetrias.</li>
    
    
  <li><strong>Tambet Matiisen</strong> <a href="https://github.com/tambetm/">Tambet</a> é um estudante de doutorado da University of Tartu, na Estonia. Anteriormente trabalhou como engenheiro de software e fundou sua própria <a href="http://www.korteriyhistu.net/">startup</a>. Seus projetos recentes variam de <a href="http://arxiv.org/abs/1511.08779">fazer agentes de deep reinforcement learning côoperarem</a> para predizer a posição de um rato a partir de suas atividades cerebrais. Ele também escreveu uma <a href="http://neuro.cs.ut.ee/demystifying-deep-reinforcement-learning/">introdução acessível à deep Q-learning</a>.</li>

</ul>

