<h2> Tempo integral </h2>

<ul class='HiresList'>
    
    
  <li><strong>Dario Amodei.</strong> <a href="https://www.linkedin.com/in/dario-amodei-3934934">Dario</a> foi um dos autores principais do <a href="https://arxiv.org/abs/1512.02595">Deep Speech 2</a>, um sistema de fala que alcançou desempenho quase humano em muitas tarefas de fala. Ele também é co-autor principal do “<a href="https://arxiv.org/abs/1606.06565">Problemas Concretos de Segurança em IA</a>”, que destaca questões relacionadas a acidentes em sistemas de machine learning. Antes da OpenAI, ele trabalhou no Google Brain.</li>
    
  <li><strong>Filip Wolski</strong> O passado recente de <a href="https://www.linkedin.com/in/filip-wolski-a7273728">Filip</a> é em modelagem "prática", tendo passado os últimos anos trabalhando na area de negociação de alta frequência. No passado ele gostava de resolver problemas em competições de programação, e ganhou o <a href="http://stats.ioinformatics.org/people/768">IOI</a> e <a href="https://icpc.baylor.edu/community/history-icpc-2007">ACM ICPC</a>.</li>
    
  <li><strong>Jack Clark.</strong> <a href="https://twitter.com/jackclarksf">Jack</a> passou os últimos anos escrevendo sobre inteligência artificial e sistemas distribuídos, mais recentemente na Bloomberg e BusinessWeek. Seus artigos abordaram tecnologias como <a href="http://www.bloomberg.com/news/features/2015-06-09/the-future-of-computers-is-the-mind-of-a-toddler">redes de memória</a>, <a href="http://www.bloomberg.com/news/articles/2015-12-02/computers-learn-how-to-paint-whatever-you-tell-them-to">geração de imagens</a>, e <a href="http://www.bloomberg.com/features/2015-preschool-for-robots/">reinforcement learning para robôs</a>, e questões como a <a href="http://www.bloomberg.com/news/articles/2016-06-23/artificial-intelligence-has-a-sea-of-dudes-problem">diversidade dentro da IA</a>. Como nosso Diretor de Estratégia e Comunicação, ele ajudará com o alcance comunitário, políticas, comunicações e estratégia.</li>

  <li><strong>Scott Gray.</strong> <a href="https://twitter.com/scottgray76">Scott</a> foi anteriormente um engenheiro na <a href="https://www.nervanasys.com/author/scott-g/">Nervana Systems</a> onde se concentrou em otimizar o desempenho de deep networks em GPUs. Suas <a href="http://arxiv.org/abs/1509.09308">otimizações em assembly-level</a> para álgebra linear e convolução densa continuam sendo as mais rápidas disponíveis. Quando não está <a href="https://github.com/scott-gray">escrevendo software</a>, costuma gastar seu tempo lendo as pesquisas mais atuais sobre neurociência e campos relacionados.</li>
    
  <li><strong>Zain Shah.</strong> <a href="http://twitter.com/zan2434">Zain</a> anteriormente liderou esforços em deep learning para construir um sistema inteligente humano-máquina colaborativo na <a href="http://claralabs.com">Clara Labs</a>. Ele também trabalhou em <a href="http://cepstral.com">síntese de fala</a> e <a href="http://cnbc.cmu.edu">neurociência computacional</a>, construiu o <a href="http://mosaic.io">Mosaic</a>, e fundou uma <a href="http://watchsend.com">empresa</a> de análise comportamental mobile. Ele construiu recentemente um <a href="http://deepgif.tarzain.com">motor de busca de GIF</a> usando deep multimodal embeddings.</li>

</ul>


